#include "minitalk.h"

void	ft_write_bit_unix(int signum, siginfo_t *info, void *context)
{
	static int	i = 0;
	static int	bit = 0;
	int			j;

	(void) context;
	j = 1;
	if (signum == SIGUSR1)
	{
		j <<= i;
		bit |= j;
	}
	i++;
	if (i == 32)
	{
		write (1, &bit, 1);
		bit = 0;
		i = 0;
		kill(info->si_pid, SIGUSR1);
	}
}

int	main(void)
{
	struct sigaction	sa;

	sa.sa_sigaction = ft_write_bit_unix;
	sa.sa_flags = SA_SIGINFO;
	sigemptyset(&sa.sa_mask);
	ft_printf("Pid : %d\n", getpid());
	sigaction(SIGUSR1, &sa, NULL);
	sigaction(SIGUSR2, &sa, NULL);
	while (1)
		pause();
}
